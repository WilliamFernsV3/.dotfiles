local M = {}


M.general = {
  i = {
    ["<C-b>"] = { "<ESC>^i", "Beginning of line" },
    ["<C-e>"] = { "<End>", "End of line" },
  },

  n = {
    ["K"] = { ":move .-2<CR>==", "Move Line Up" },
    ["J"] = { ":move .+1<CR>==", "Move Line Down" },
    ["<A-p>"] = { "<C-o>zz", desc = "Jump To Previous " },
    ["<A-n>"] = { "<C-i>zz", desc = "Jump To Next" },
    ['<Esc>'] = { '<cmd>nohlsearch<CR>', desc = "Remove search highlights", opts = { noremap = true, silent = true } },
    ["W"] = { "mzJ`z", "Join lines" },
    ["<C-d>"] = { "<C-d>zz", "Scrolls down half a page" },
    ["<C-u>"] = { "<C-u>zz", "Scrolls up half a page" },
    ["n"] = { "nzzzv", "Moves to the next search result" },
    ["N"] = { "Nzzzv", "Moves to the previous search result" },
    ["Q"] = { "<nop>", "Remap 'Q' to no operation" },
    ["<C-f>"] = { "<cmd>silent !tmux neww tmux-sessionizer<CR>", "new tmux window using tmux-sessionizer." },
    ["<leader>j"] = { "<cmd>cnext<CR>zz", "Moves to the next item in the quickfix list" },
    ["<leader>k"] = { "<cmd>cprev<CR>zz", "Moves to the previous item in the quickfix list" },
    ["<leader><C-k>"] = { "<cmd>lnext<CR>zz", "Moves to the next item in the location list" },
    ["<leader><C-j>"] = { "<cmd>lprev<CR>zz", "Moves to the previous item in the location list" },
    ["<leader>s"] = { [[:%s/\<<C-r><C-w>\>/<C-r><C-w>/gI<Left><Left><Left>]], "Searches and replaces word cursor to be case-insensitive" },
    ["<leader>vpp"] = { "<cmd>e ~/.config/nvim/lua/custom/plugins.lua<CR>", "Open 'plugins.lua'" },
    ["<leader>pv"] = { "<cmd>Ex <CR>", "File Explorer" },
    ["<A-b>"] = { "<C-^>", "Previous Open Buffer" },
    ["<C-s>"] = { "<cmd> w <CR>", "Save file" },

    -- Copy all
    ["<leader>cp"] = { "<cmd> %y+ <CR>", "Copy whole file" },

    -- line numbers
    ["<leader>n"] = { "<cmd> set nu! <CR>", "Toggle line number" },
    ["<leader>rn"] = { "<cmd> set rnu! <CR>", "Toggle relative number" },

    -- Allow moving the cursor through wrapped lines with j, k, <Up> and <Down>
    -- http://www.reddit.com/r/vim/comments/2k4cbr/problem_with_gj_and_gk/
    -- empty mode is same as using <cmd> :map
    -- also don't use g[j|k] when in operator pending mode, so it doesn't alter d, y or c behaviour
    ["j"] = { 'v:count || mode(1)[0:1] == "no" ? "j" : "gj"', "Move down", opts = { expr = true } },
    ["k"] = { 'v:count || mode(1)[0:1] == "no" ? "k" : "gk"', "Move up", opts = { expr = true } },
    ["<Up>"] = { 'v:count || mode(1)[0:1] == "no" ? "k" : "gk"', "Move up", opts = { expr = true } },
    ["<Down>"] = { 'v:count || mode(1)[0:1] == "no" ? "j" : "gj"', "Move down", opts = { expr = true } },

    -- new buffer
    ["<leader>b"] = { "<cmd> enew <CR>", "New buffer" },
    ["<leader>ch"] = { "<cmd> NvCheatsheet <CR>", "Mapping cheatsheet" },

    ["<leader>fm"] = {
      function()
        vim.lsp.buf.format { async = true }
      end,
      "LSP formatting",
    },
  },

  t = { ["<C-x>"] = { vim.api.nvim_replace_termcodes("<C-\\><C-N>", true, true, true), "Escape terminal mode" }, },
  v = {
    ["W"] = { ":s/\\n\\s*//g<CR>gv", opts = { noremap = true, silent = true }, desc = "Join lines and remove extra spaces in visual mode" },
    ["K"] = { ":move '<-2<CR>gv=gv", "Move Selected Text Up" },
    ["J"] = { ":move '>+1<CR>gv=gv", "Move Selected Text Down" },
    ["<"] = { "<gv", "Indent line" },
    [">"] = { ">gv", "Indent line" }
  },

  x = {
    ["j"] = { 'v:count || mode(1)[0:1] == "no" ? "j" : "gj"', "Move down", opts = { expr = true } },
    ["k"] = { 'v:count || mode(1)[0:1] == "no" ? "k" : "gk"', "Move up", opts = { expr = true } },
    -- Don't copy the replaced text after pasting in visual mode
    -- https://vim.fandom.com/wiki/Replace_a_word_with_yanked_text#Alternative_mapping_for_paste
    ["p"] = { 'p:let @+=@0<CR>:let @"=@0<CR>', "Dont copy replaced text", opts = { silent = true } },
  },
}

